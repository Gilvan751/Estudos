# Exercício 01 - área do retângulo

print("Calcule a area de um retangulo")

base = float(input("Qual o tamanho da base do seu retangulo em metros?   "))
altura = float(input("Qual o tamanho da altura do seu retangulo em metros?  "))
area = base * altura

print(f'O seu retangulo de {base:.1f} m  de base por {altura:.1f} m de altura possui area de : {area:.1f}  m²')
